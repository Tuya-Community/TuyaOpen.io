/** Replace {0},{1},… placeholders. */
export function fmt(str, ...args) {
  if (!str) return ''
  return str.replace(/\{(\d+)\}/g, (_, i) => args[i] ?? '')
}

/** Resolve a locale string ("en" | "zh" | anything) to en/zh. */
export function pickLocale(locale) {
  return locale === 'zh' ? 'zh' : 'en'
}
